package com.edu;

public class DataTypes {
	public static void main(String[] args)
	{
		int i=10;
		float f=10.11f;
		long l=123456;
		double d=13.2345678;
		System.out.println("data type int "+i);
		System.out.println("data type float "+f);
		System.out.println("data type long "+l);
		System.out.println("data type double "+d);
		System.out.println();
	}

}
